package com.example.kwons.music_in_you;

public class MusicPreference {
    String happy_pref;
    String sad_pref;
    String angry_pref;
    String normal_pref;
    String calm_pref;

    public String getHappy_pref() {
        return happy_pref;
    }

    public void setHappy_pref(String happy_pref) {
        this.happy_pref = happy_pref;
    }

    public String getSad_pref() {
        return sad_pref;
    }

    public void setSad_pref(String sad_pref) {
        this.sad_pref = sad_pref;
    }

    public String getAngry_pref() {
        return angry_pref;
    }

    public void setAngry_pref(String angry_pref) {
        this.angry_pref = angry_pref;
    }

    public String getNormal_pref() {
        return normal_pref;
    }

    public void setNormal_pref(String normal_pref) {
        this.normal_pref = normal_pref;
    }

    public String getCalm_pref() {
        return calm_pref;
    }

    public void setCalm_pref(String calm_pref) {
        this.calm_pref = calm_pref;
    }
}
